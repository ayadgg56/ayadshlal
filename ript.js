// -------------------- عرض البطاقات --------------------
function displayIssues(issuesToShow) {
    issuesGrid.innerHTML = '';
    
    if (!issuesToShow || issuesToShow.length === 0) {
        issuesGrid.innerHTML = '<p class="no-results">لا توجد أعطال متاحة</p>';
        return;
    }
    
    issuesToShow.forEach(issue => {
        const card = document.createElement('div');
        card.className = 'issue-card';
        card.dataset.id = issue.id;
        
        // تحديد لون وأيقونة حسب نوع العطل
        let typeBadge = '';
        if (issue.type === 'Hardware') {
            typeBadge = '<span class="type-badge hardware">🔧 هاردوير</span>';
        } else if (issue.type === 'Software') {
            typeBadge = '<span class="type-badge software">💻 سوفتوير</span>';
        } else if (issue.type === 'Both') {
            typeBadge = '<span class="type-badge both">🔧💻 هاردوير/سوفتوير</span>';
        }
        
        const devicesList = Array.isArray(issue.devices) ? issue.devices : [issue.devices];
        const symptomsList = Array.isArray(issue.symptoms) ? issue.symptoms : [issue.symptoms];
        
        card.innerHTML = `
            <h3>${issue.title}</h3>
            <div style="display: flex; gap: 0.5rem; flex-wrap: wrap; margin-bottom: 0.5rem;">
                <span class="category">${issue.category}</span>
                ${typeBadge}
            </div>
            <div class="devices">📱 ${devicesList.join(' - ')}</div>
            <div class="symptoms">⚠️ أهم الأعراض: ${symptomsList.slice(0, 2).join('، ')}...</div>
        `;
        
        card.addEventListener('click', () => openModal(issue));
        issuesGrid.appendChild(card);
    });
}

