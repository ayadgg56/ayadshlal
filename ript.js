// -------------------- بحث وتصفية --------------------
function filterIssues() {
    const searchTerm = searchInput.value.toLowerCase();
    const category = categoryFilter.value;
    
    if (category === 'about') {
        showAboutSection();
        return;
    }
    
    const filtered = issues.filter(issue => {
        const matchesSearch = issue.title.toLowerCase().includes(searchTerm) ||
            (Array.isArray(issue.symptoms) && issue.symptoms.some(s => s.toLowerCase().includes(searchTerm))) ||
            (Array.isArray(issue.causes) && issue.causes.some(c => c.toLowerCase().includes(searchTerm)));
        const matchesCategory = category === 'all' || issue.category === category;
        return matchesSearch && matchesCategory;
    });
    
    displayIssues(filtered);
}

